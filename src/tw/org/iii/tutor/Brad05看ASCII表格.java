package tw.org.iii.tutor;

public class Brad05看ASCII表格 {

	public static void main(String[] args) {
		char c1 = 'a';	// 97
		char c2 = 65;	// char2^16 => 0 ~ 65535
		System.out.println(c1);
		System.out.println(c2);
		int var1 = c1 + c2;
		System.out.println(var1);
		char c3 = 'A'; //看ASCII code碼十進位
		int var2 = c3;
		System.out.println(var2);
		//char代表著字元（character）型別，用來表示一個字母、數字、符號或其他可印刷的字符。
		
	}

}
